/*
    Formas de crear Variable
*/

// Forma uno (mala practica)
nombre = "Juan";
console.log(nombre);

// Forma dos
var nombre2 = "Carlos";
console.log(nombre2);

// Forma tres (recomendada)
let nombre3 = "Ivan";
console.log(nombre3);

//Forma cuatro (variables que no cambian sus valores iniciales)
const clave = '0dop';
console.log(clave);