// Un objeto puedo contener propiedades y metodos

let persona = {
    nombre: 'Juan',
    apellido: 'Perez',
    email: 'juanp@mail.com',
    edad: 28
}

// objeto.propiedad
console.log(persona.nombre);
console.log(persona.edad);
console.log(persona.apellido);

// Acceder a todo el objeto
console.log(persona);
