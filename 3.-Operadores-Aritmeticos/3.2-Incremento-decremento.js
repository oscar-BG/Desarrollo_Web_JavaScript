let a = 3, b = 2;
let z =  a+ b;

//Incremento
//Pre-icremento (el operado ++ antes de la variable)
z = ++a;
console.log(z);

//Post-incremento (el operador ++ despues de la variable)
z = b++;
console.log(z);


//Decremento
//Predecremento
z = --a;
console.log(z);

//Posdecremento
z = b--;
console.log(b);
console.log(z);